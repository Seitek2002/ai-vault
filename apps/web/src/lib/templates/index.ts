// ── ProseMirror node helpers ─────────────────────────────────────────────────

const txt  = (text: string) => ({ type: "text", text });
const bold = (text: string) => ({ type: "text", text, marks: [{ type: "bold" }] });

/** Paragraph containing one or more inline nodes */
const p = (...nodes: object[]) => ({ type: "paragraph", content: nodes });
/** Paragraph with plain text */
const para = (text: string) => ({ type: "paragraph", content: [txt(text)] });
/** Paragraph with all-bold text */
const boldPara = (text: string) => ({ type: "paragraph", content: [bold(text)] });
/** Centered paragraph */
const centered = (text: string) => ({
  type: "paragraph",
  attrs: { textAlign: "center" },
  content: [txt(text)],
});
/** Centered bold paragraph */
const centeredBold = (text: string) => ({
  type: "paragraph",
  attrs: { textAlign: "center" },
  content: [bold(text)],
});
const emptyPara = () => ({ type: "paragraph" });

/** Level-1 heading, centered */
const h1 = (text: string) => ({
  type: "heading",
  attrs: { level: 1, textAlign: "center" },
  content: [txt(text)],
});
/** Level-2 heading, left-aligned */
const h2 = (text: string) => ({
  type: "heading",
  attrs: { level: 2 },
  content: [txt(text)],
});
/** Level-3 heading, left-aligned */
const h3 = (text: string) => ({
  type: "heading",
  attrs: { level: 3 },
  content: [txt(text)],
});

/** Standard table with header row + N empty data rows */
const table = (headers: string[], rows: number) => ({
  type: "table",
  content: [
    {
      type: "tableRow",
      content: headers.map((h) => ({
        type: "tableHeader",
        content: [{ type: "paragraph", content: [txt(h)] }],
      })),
    },
    ...Array.from({ length: rows }, () => ({
      type: "tableRow",
      content: headers.map(() => ({ type: "tableCell", content: [emptyPara()] })),
    })),
  ],
});

/** Table row with a bold "Итого" label spanning to the last column */
const itogoPara = (label = "Итого") =>
  ({ type: "paragraph", content: [bold(label)] });

/** Services table for AVR/Счёт: includes one "Итого" row */
const servicesTable = (headers: string[], dataRows: number) => ({
  type: "table",
  content: [
    {
      type: "tableRow",
      content: headers.map((h) => ({
        type: "tableHeader",
        content: [{ type: "paragraph", content: [{ type: "text", text: h, marks: [{ type: "bold" }] }] }],
      })),
    },
    ...Array.from({ length: dataRows }, () => ({
      type: "tableRow",
      content: headers.map(() => ({ type: "tableCell", content: [emptyPara()] })),
    })),
    // Итого row: empty cells except last two → "Итого" label + empty amount
    {
      type: "tableRow",
      content: headers.map((_, i) => ({
        type: "tableCell",
        content: [
          i === headers.length - 2
            ? itogoPara("Итого")
            : emptyPara(),
        ],
      })),
    },
  ],
});

/** 2-column table for РЕКВИЗИТЫ И ПОДПИСИ СТОРОН */
const rekvTable = (leftParas: object[], rightParas: object[]) => ({
  type: "table",
  content: [
    {
      type: "tableRow",
      content: [
        { type: "tableCell", content: leftParas },
        { type: "tableCell", content: rightParas },
      ],
    },
  ],
});

